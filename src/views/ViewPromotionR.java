    public void addTable() {
        dtm = (DefaultTableModel) tbkmp.getModel();
        dtm.setRowCount(0);
        for (PromotionR pr : this.promoRservice.getAll()) {
            dtm.addRow(new Object[]{
                pr.getCode(),
                pr.getValue(),
                pr.getDateStart(),
                pr.getDateEnd(),});
        }
    }

    private PromotionR getData() {
        ReadWriteData readWriteData = new ReadWriteData();
        PromotionR pr = new PromotionR();
        RandomCode ran = new RandomCode();
        String code = ran.createCode("GP", "GiamGiaPhong.txt");
        for (PromotionR promotionR : promoRservice.getAll()) {
            if (promotionR.getCode().equals(code)) {
                try {
                    readWriteData.ghidl(Integer.parseInt(promotionR.getCode().substring(2)), "GiamGiaPhong.txt");
                    getData();
                } catch (IOException ex) {
                    Logger.getLogger(ViewPromotionR.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
        String value = this.txtgt.getText();
        String start = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csndb.getDate()));
        String end = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csnkt.getDate()));
        pr.setCode(code);
        pr.setDateEnd(end);
        pr.setValue(value);
        pr.setDateStart(start);
        return pr;

    }

    private PromotionR getDataDelete() {
        String code = this.lbma.getText();
        String value = this.txtgt.getText();
        String start = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csndb.getDate()));
        String end = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csnkt.getDate()));

        PromotionR pr = new PromotionR();
        pr.setCode(code);
        pr.setDateEnd(end);
        pr.setDateStart(start);
        pr.setValue(value);
        return pr;
    }

    private String getIDPromotionR() {
        PromotionR Pro = this.getDataDelete();
        String id = "";
        for (PromotionR pro : this.promoRservice.getAll()) {
            if (Pro.getCode().equals(pro.getCode())) {
                id = pro.getId();
                break;
            }
        }
        return id;
    }

