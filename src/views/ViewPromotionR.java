    private void tbkmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbkmpMouseClicked
        int row = tbkmp.getSelectedRow();
        lbma.setText(tbkmp.getValueAt(row, 0).toString());
        txtgt.setText(tbkmp.getValueAt(row, 1).toString());
        String nbd = tbkmp.getValueAt(row, 2).toString();
        if (nbd.indexOf("-") != -1) {
            nbd = (nbd.substring(8) + "/" + nbd.substring(5, 7) + "/" + nbd.substring(0, 4));
        }
        try {
            csndb.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(nbd));
        } catch (ParseException ex) {
            Logger.getLogger(ViewTrangChu.class.getName()).log(Level.SEVERE, null, ex);
        }
        String nkt = tbkmp.getValueAt(row, 3).toString();
        if (nkt.indexOf("-") != -1) {
            nkt = (nbd.substring(8) + "/" + nkt.substring(5, 7) + "/" + nkt.substring(0, 4));
        }
        try {
            csnkt.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(nkt));
        } catch (ParseException ex) {
            Logger.getLogger(ViewTrangChu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbkmpMouseClicked

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        if (txtgt.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin!");
        } else {
            String mess = promoRservice.insert(getData());
            JOptionPane.showMessageDialog(this, mess);
            addTable();
            clearForm();
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        int row = tbkmp.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa!");
        } else {
            PromotionR room = this.getDataDelete();
            promoRservice.update(room, getIDPromotionR());
            addTable();
        }
    }//GEN-LAST:event_btnsuaActionPerformed

