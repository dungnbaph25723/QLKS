    private void tb_khuyenmaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_khuyenmaiMouseClicked
        int row = tb_khuyenmai.getSelectedRow();
        txtma.setText(tb_khuyenmai.getValueAt(row, 0).toString());
        txtgt.setText(tb_khuyenmai.getValueAt(row, 1).toString());
        String nbd = tb_khuyenmai.getValueAt(row, 2).toString();
        if (nbd.indexOf("-") != -1) {
            nbd = (nbd.substring(8) + "/" + nbd.substring(5, 7) + "/" + nbd.substring(0, 4));
        }
        try {
            csndb.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(nbd));
        } catch (ParseException ex) {
            Logger.getLogger(ViewTrangChu.class.getName()).log(Level.SEVERE, null, ex);
        }
        String nkt = tb_khuyenmai.getValueAt(row, 3).toString();
        if (nkt.indexOf("-") != -1) {
            nkt = (nbd.substring(8) + "/" + nkt.substring(5, 7) + "/" + nkt.substring(0, 4));
        }
        try {
            csnkt.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(nkt));
        } catch (ParseException ex) {
            Logger.getLogger(ViewTrangChu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tb_khuyenmaiMouseClicked

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        if (txtgt.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin!");
        } else {
            String mess = promotionSService.insert(getData());
            JOptionPane.showMessageDialog(this, mess);
            addTable();
            clearForm();
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        int row = tb_khuyenmai.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa!");
        } else {
            PromotionS room = this.getDataDelete();
            JOptionPane.showMessageDialog(this, promotionSService.update(getIDPromotionS(), room));
            addTable();
        }
    }//GEN-LAST:event_btnsuaActionPerformed

