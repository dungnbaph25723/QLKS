    public void addTable() {
        dtm = (DefaultTableModel) tb_khuyenmai.getModel();
        dtm.setRowCount(0);
        for (PromotionS x : this.promotionSService.getList()) {
            dtm.addRow(new Object[]{
                x.getCode(),
                x.getValue(),
                x.getDateStart(),
                x.getDateEnd()
            });
        }
    }

    private PromotionS getData() {
        ReadWriteData readWriteData = new ReadWriteData();
        RandomCode ran = new RandomCode();
        String code = ran.createCode("GG", "GiamGiaDV.txt");
        for (PromotionS p : promotionSService.getList()) {
            if (p.getCode().equals(code)) {
                try {
                    readWriteData.ghidl(Integer.parseInt(p.getCode().substring(2)), "GiamGiaDV.txt");
                    getData();
                } catch (IOException ex) {
                    Logger.getLogger(ViewPromotionS.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
        String value = this.txtgt.getText();
        String start = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csndb.getDate()));
        String end = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csnkt.getDate()));

        PromotionS pr = new PromotionS("", code, value, start, end);
        return pr;

    }

    private PromotionS getDataDelete() {
        String code = this.txtma.getText();
        String value = this.txtgt.getText();
        String start = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csndb.getDate()));
        String end = String.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(csnkt.getDate()));

        PromotionS pr = new PromotionS("", code, value, start, end);
        return pr;

    }

    private String getIDPromotionS() {
        PromotionS Pro = this.getDataDelete();
        String id = "";
        for (PromotionS p : this.promotionSService.getList()) {
            if (Pro.getCode().equals(p.getCode())) {
                id = p.getId();
                break;
            }
        }
        return id;
    }

